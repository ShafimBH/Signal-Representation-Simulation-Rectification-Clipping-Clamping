% --- Executes on button press in pushbutton1.
function pushbutton1_Callback(hObject, eventdata, handles)
% hObject    handle to pushbutton1 (see GCBO)
% eventdata  reserved - to be defined in a future version of MATLAB
% handles    structure with handles and user data (see GUIDATA)
V_p=str2num(get(handles.amp,'String'));
f=str2num(get(handles.freq,'String'));
C=abs(str2num(get(handles.cap,'String')));
R=abs(str2num(get(handles.R,'String')));
V_p=abs(V_p);
a=get(handles.listbox1,'Value');
switch a
    case 1
        V_r=double(V_p/(C*f*R));
    case 2
        V_r=double(V_p/(2*C*f*R));
end
set(handles.Vr,'String',V_r);






